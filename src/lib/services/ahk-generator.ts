function dedupeIssues(issues: ExportCompatibilityIssue[]): ExportCompatibilityIssue[] {
	const seen = new Set<string>();
	return issues.filter((issue) => {
		const signature = [issue.code, issue.layerName, issue.keyId, issue.message].join('::');
		if (seen.has(signature)) return false;
		seen.add(signature);
		return true;
	});
}

function escapeAhkString(value: string): string {
	return value.replaceAll('`', '``').replaceAll('"', '""');
}

// AHK v2 文字列リテラルの安全なクォート選択
// " を含む → シングルクォートで囲む（' は '' にエスケープ）
// それ以外 → ダブルクォートで囲む（` は `` に、" は "" にエスケープ）
function quoteForAhk(value: string): string {
	if (value.includes('"')) {
		return `'${value.replaceAll("'", "''")}'`;
	}
	return `"${value.replaceAll('\`', '\`\`')}"`;
}

// Send コマンド内のテキストトークン用個別エスケープ（修飾キー付きテキストモード専用）
function escapeAhkSendSpecials(value: string): string {
	return value
		.replaceAll('`', '``')
		.replace(/[{}]/g, (ch) => ch === '{' ? '{{}' : '{}}')
		.replaceAll('^', '{^}')
		.replaceAll('!', '{!}')
		.replaceAll('+', '{+}')
		.replaceAll('#', '{#}')
		.replaceAll('"', '""');
}

function indent(lines: string[]): string[] {
	return lines.map((line) => `    ${line}`);
}

function trimTrailingBlankLine(lines: string[]): void {
	while (lines.at(-1) === '') {
		lines.pop();
	}
}